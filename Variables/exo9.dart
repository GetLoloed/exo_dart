void main() {
  var num1 = 24;
  var num2 = 7;

  var sum = num1 + num2;
  var difference = num1 - num2;
  var product = num1 * num2;
  var quotient = num1 / num2;
  var remainder = num1 % num2;

  print('$num1 + $num2 = $sum');
  print('$num1 - $num2 = $difference');
  print('$num1 * $num2 = $product');
  print('$num1 / $num2 = $quotient');
  print('$num1 % $num2 = $remainder');
}